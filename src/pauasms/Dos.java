    public String Hash(String c)
    {
    try{
    MessageDigest msgDigest = MessageDigest.getInstance("MD5");
     msgDigest.update((new String(c)).getBytes("UTF8"));
     String passHash = new String(msgDigest.digest());
     return passHash;
     
     
    }
    
    catch (Exception ex)
    {
    return c;
    
    }
    }
    
    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        try {
            String username = jLabel2.getText(); // Assuming you store the logged-in username here
            String oldPassword = new String(jPasswordField1.getPassword());
            String newPassword = new String(jPasswordField2.getPassword());

            if (oldPassword.isEmpty() || newPassword.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter both old and new passwords");
                return;
            }

            if (oldPassword.equals(newPassword)) {
                JOptionPane.showMessageDialog(this, "New password cannot be the same as old password");
                return;
            }

            String hashedOldPassword = Hash(oldPassword);
            String hashedNewPassword = Hash(newPassword);

            try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/asms_schema", "root", "root..")) {

            try (PreparedStatement ps = con.prepareStatement(
                "SELECT password, useremail FROM userdetails WHERE username = ?")) {
            ps.setString(1, username);

            try (ResultSet rs = ps.executeQuery()) {
                if (!rs.next()) {
                    JOptionPane.showMessageDialog(this, "User not found");
                    return;
                }

                String storedPassword = rs.getString("password");
                if (!hashedOldPassword.equals(storedPassword)) {
                    JOptionPane.showMessageDialog(this, "Current password is incorrect");
                    return;
                }

                try (PreparedStatement updatePs = con.prepareStatement(
                    "UPDATE userdetails SET password = ? WHERE username = ?")) {
                updatePs.setString(1, hashedNewPassword);
                updatePs.setString(2, username);

                int updated = updatePs.executeUpdate();
                if (updated > 0) {
                    logPasswordChangeActivity(con, username);

                    String email = rs.getString("useremail");
                    sendPasswordChangeEmail(username, email, newPassword);

                    JOptionPane.showMessageDialog(this, "Password changed successfully");
                    clearPasswordFields();
                } else {
                    JOptionPane.showMessageDialog(this, "Password update failed");
                }
            }
        }
        }
        }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                "Error changing password: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
        }

