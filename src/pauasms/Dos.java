    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
    Date selectedDate = jDateChooser1.getDate();
    String staffId = jTextField1.getText().trim();
    String unit = (String) jComboBox2.getSelectedItem();

    if ((selectedDate == null) && staffId.isEmpty() && 
        (unit == null || unit.equalsIgnoreCase("All") || unit.trim().isEmpty())) {
        JOptionPane.showMessageDialog(this, "Please select at least one filter", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try (Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/asms_schema",
            "root",
            "root..")) {

        DefaultTableModel model = (DefaultTableModel) jTable6.getModel();
        model.setRowCount(0);

        if (model.getColumnCount() == 0) {
            model.setColumnIdentifiers(new String[]{
                "User ID", "Name", "Sign in Time",
                "Sign out Time", "Sign in Status", "Sign out Status"
            });
        }

        StringBuilder queryBuilder = new StringBuilder("""
            SELECT sl.*, ud.user_id
            FROM signin_log sl
            JOIN userdetails ud ON sl.name = ud.name
            WHERE 1 = 1
        """);

        List<Object> parameters = new ArrayList<>();

        if (selectedDate != null) {
            queryBuilder.append(" AND DATE(sl.sign_in_time) = ?");
            parameters.add(new java.sql.Date(selectedDate.getTime()));
        }

        if (!staffId.isEmpty()) {
            try {
                int staffIdInt = Integer.parseInt(staffId);
                queryBuilder.append(" AND ud.user_id = ?");
                parameters.add(staffIdInt);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Staff ID must be numeric.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        if (unit != null && !unit.equalsIgnoreCase("All") && !unit.trim().isEmpty()) {
            queryBuilder.append(" AND ud.unit = ?");
            parameters.add(unit);
        }

        queryBuilder.append(" ORDER BY sl.sign_in_time DESC");

        try (PreparedStatement psa = con.prepareStatement(queryBuilder.toString())) {
            for (int i = 0; i < parameters.size(); i++) {
                psa.setObject(i + 1, parameters.get(i));
            }

            try (ResultSet rs = psa.executeQuery()) {
                boolean hasResults = false;

                while (rs.next()) {
                    hasResults = true;
                    model.addRow(new Object[]{
                        rs.getInt("user_id"),
                        rs.getString("name"),
                        formatDateTime(rs.getTimestamp("sign_in_time")),
                        formatDateTime(rs.getTimestamp("sign_out_time")),
                        rs.getString("sign_in_status"),
                        rs.getString("sign_out_status")
                    });
                }

                if (!hasResults) {
                    JOptionPane.showMessageDialog(this,
                            "No records found for the selected filters",
                            "Information",
                            JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,
                "Database error: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,
                "Unexpected error: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
    }

        // TODO add your handling code here:
        }

