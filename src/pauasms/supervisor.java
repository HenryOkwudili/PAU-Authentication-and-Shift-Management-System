private void logPasswordChangeActivity(Connection con, String username) throws SQLException {
    try (PreparedStatement ps = con.prepareStatement(
            "INSERT INTO useractivities (username, activitytype, details) VALUES (?, ?, ?)")) {
        ps.setString(1, username);
        ps.setString(2, "Password Change");
        ps.setString(3, "User changed their own password");
        ps.executeUpdate();
    }
}

